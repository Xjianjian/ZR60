void MyString_PutCharAt(char ch,FONTTYPE fonttype,short x,short y,
								unsigned short Fontcolor,
								unsigned short Bkcolor,
								unsigned char isTransParent)
{
	unsigned char *font;
	unsigned char height,width,i,j,k;
	unsigned short index=0;
	switch(fonttype)
	{
		case FONT_BLACK:
		height=32;
		width=16;
		switch (ch)
		{
			case '.':
				index=10;
			break;
			case 'K':
				index=11;
			break;
			case 'M':
				index=12;
			break;
			case '/':
				index=13;
			break;
			case 'H':
				index=14;
			break;
			default:
			if(ch>=48&&ch<58)
			{
				index=ch-48;
			}
			break;
		}
		font=(unsigned char *)Font_Black+index*height*width/8;
		break;
		case FONT_CH_64x64:
			height=48;
			width=48;
			font=(unsigned char *)Font_CH_64x64+(ch-1)*height*width/8;
		break;
		default:
			return;	
	}
	width/=8;
	for(i=0;i<height;i++)
	{
		for(j=0;j<width;j++)
		{
			unsigned char tmp=*(font+i*width+j);
			for(k=0;k<8;k++)
			{
				if((tmp>>k)&0x01)
				{
					GUICore_DrawPoint(x+(j+1)*8-k,y+i,Fontcolor);
				}
				else
				{
					if(isTransParent!=TRANSPARENT)
					GUICore_DrawPoint(x+(j+1)*8-k,y+i,Bkcolor);
				}
			}
		}
	}
}
